#!/usr/bin/env bash
# Post results: PR review or baseline commit.
# See docs/specs/2026-04-24-standalone-eval-runner-design.md Section 3.
#
# Usage:
#   post-results.sh pr       --workspace <dir> [--workspace <dir> ...]
#   post-results.sh baseline --workspace <dir> --evals-dir <path> --commit-hash <hash>

set -euo pipefail

if [[ -z "${GH_TOKEN:-}" && -z "${GITHUB_TOKEN:-}" ]]; then
    echo "Error: GH_TOKEN or GITHUB_TOKEN must be set for gh CLI authentication" >&2
    exit 1
fi

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REVIEW_MARKER="## Skill Eval Results"

detect_pr_number() {
    local pr_num="${PR_NUMBER:-}"
    if [[ -z "$pr_num" ]]; then
        pr_num=$(gh pr view --json number -q '.number' 2>/dev/null || true)
    fi
    if [[ -z "$pr_num" || ! "$pr_num" =~ ^[0-9]+$ ]]; then
        echo "Error: could not detect valid PR number. Are you in a PR context?" >&2
        exit 1
    fi
    echo "$pr_num"
}

MODE="${1:-}"
[[ -z "$MODE" ]] && echo "Usage: post-results.sh {pr|baseline|comment-reply} [options]" >&2 && exit 1
shift

case "$MODE" in
    pr)
        WORKSPACES=()
        while [[ $# -gt 0 ]]; do
            case "$1" in
                --workspace) WORKSPACES+=("$2"); shift 2 ;;
                *) echo "Error: unknown argument: $1" >&2; exit 1 ;;
            esac
        done

        [[ ${#WORKSPACES[@]} -eq 0 ]] && echo "Error: at least one --workspace required" >&2 && exit 1

        # Build combined review body
        BODY="$REVIEW_MARKER"$'\n'
        for ws in "${WORKSPACES[@]}"; do
            if [[ -f "$ws/summary.md" ]]; then
                BODY+=$'\n'"$(cat "$ws/summary.md")"$'\n'$'\n'"---"$'\n'
            else
                BODY+=$'\n'"**Warning:** No summary found for $(basename "$ws")"$'\n'
            fi
        done

        PLUGIN_JSON="$SCRIPT_DIR/../.claude-plugin/plugin.json"
        if [[ -f "$PLUGIN_JSON" ]]; then
            if VERSION=$(jq -er '.version' "$PLUGIN_JSON" 2>/dev/null); then
                BODY+=$'\n'"*Generated by [skill-litmus](https://github.com/mrizzi/skill-litmus) v${VERSION}*"$'\n'
            fi
        fi

        PR_NUMBER=$(detect_pr_number)

        # Find existing skill-litmus review to update
        REVIEW_ID=$(gh api "repos/{owner}/{repo}/pulls/$PR_NUMBER/reviews" \
            --jq "[.[] | select(.user.login == \"github-actions[bot]\" and (.body | startswith(\"$REVIEW_MARKER\"))) | .id] | first // empty" \
            2>/dev/null || true)

        if [[ -n "$REVIEW_ID" ]]; then
            gh api "repos/{owner}/{repo}/pulls/$PR_NUMBER/reviews/$REVIEW_ID" \
                -X PUT -f body="$BODY" > /dev/null
            echo "Updated existing PR review ($REVIEW_ID)"
        else
            gh api "repos/{owner}/{repo}/pulls/$PR_NUMBER/reviews" \
                -X POST -f event="COMMENT" -f body="$BODY" > /dev/null
            echo "Posted new PR review"
        fi
        ;;

    baseline)
        WORKSPACE=""
        EVALS_DIR=""
        COMMIT_HASH=""
        while [[ $# -gt 0 ]]; do
            case "$1" in
                --workspace)   WORKSPACE="$2";   shift 2 ;;
                --evals-dir)   EVALS_DIR="$2";   shift 2 ;;
                --commit-hash) COMMIT_HASH="$2"; shift 2 ;;
                *) echo "Error: unknown argument: $1" >&2; exit 1 ;;
            esac
        done

        [[ -z "$WORKSPACE" ]]   && echo "Error: --workspace required" >&2 && exit 1
        [[ -z "$EVALS_DIR" ]]   && echo "Error: --evals-dir required" >&2 && exit 1
        [[ -z "$COMMIT_HASH" ]] && echo "Error: --commit-hash required" >&2 && exit 1

        if ! [[ "$COMMIT_HASH" =~ ^[0-9a-f]{7,40}$ ]]; then
            echo "Error: invalid commit hash format: $COMMIT_HASH" >&2
            exit 1
        fi

        BASELINE_DIR="$EVALS_DIR/baselines/$COMMIT_HASH"
        mkdir -p "$BASELINE_DIR"

        for eval_dir in "$WORKSPACE"/eval-*/; do
            [ -d "$eval_dir" ] || continue
            cp -r "$eval_dir" "$BASELINE_DIR/"
        done
        for f in benchmark.json feedback.json summary.md; do
            [ -f "$WORKSPACE/$f" ] && cp "$WORKSPACE/$f" "$BASELINE_DIR/"
        done

        # Relative symlink so it works when the repo is cloned anywhere
        ln -snf "$COMMIT_HASH" "$EVALS_DIR/baselines/latest"

        git add "$EVALS_DIR/baselines/"
        if git diff --cached --quiet; then
            echo "No baseline changes to commit"
        else
            git commit -m "chore: update baseline for ${COMMIT_HASH:0:7}"
            for attempt in 1 2 3; do
                if git push; then
                    echo "Baseline pushed (attempt $attempt)"
                    break
                fi
                echo "Push failed (attempt $attempt/3), rebasing..."
                if ! git pull --rebase -X theirs origin "${BASELINE_BRANCH:-main}"; then
                    if ! git rev-parse --verify -q REBASE_HEAD >/dev/null 2>&1; then
                        echo "Error: pull failed (not a rebase conflict)" >&2
                        exit 1
                    fi
                    while IFS= read -r conflict; do
                        case "$conflict" in
                            */baselines/latest)
                                git checkout --theirs "$conflict"
                                git add "$conflict"
                                ;;
                            *)
                                echo "Error: unexpected conflict in $conflict" >&2
                                git rebase --abort 2>/dev/null || true
                                exit 1
                                ;;
                        esac
                    done < <(git diff --name-only --diff-filter=U 2>/dev/null)
                    if ! git rebase --continue 2>/dev/null; then
                        echo "Error: rebase --continue failed" >&2
                        git rebase --abort 2>/dev/null || true
                        exit 1
                    fi
                fi
                if [[ $attempt -eq 3 ]]; then
                    echo "Error: push failed after 3 attempts" >&2
                    exit 1
                fi
            done
        fi

        echo "Baseline committed: $BASELINE_DIR"
        ;;

    comment-reply)
        COMMENT_ID=""
        BODY=""
        REACTION=""
        while [[ $# -gt 0 ]]; do
            case "$1" in
                --comment-id) COMMENT_ID="$2"; shift 2 ;;
                --body)       BODY="$2";       shift 2 ;;
                --reaction)   REACTION="$2";   shift 2 ;;
                *) echo "Error: unknown argument: $1" >&2; exit 1 ;;
            esac
        done

        if [[ -n "$COMMENT_ID" && ! "$COMMENT_ID" =~ ^[0-9]+$ ]]; then
            echo "Error: invalid comment ID: $COMMENT_ID" >&2
            exit 1
        fi

        if [[ -n "$REACTION" && -n "$COMMENT_ID" ]]; then
            gh api "repos/{owner}/{repo}/issues/comments/$COMMENT_ID/reactions" \
                -X POST -f content="$REACTION" > /dev/null 2>&1 || echo "Warning: failed to add reaction" >&2
        fi

        if [[ -n "$BODY" ]]; then
            PR_NUMBER=$(detect_pr_number)
            gh api "repos/{owner}/{repo}/issues/$PR_NUMBER/comments" \
                -X POST -f body="$BODY" > /dev/null
            echo "Posted reply to PR #$PR_NUMBER"
        fi
        ;;

    *)
        echo "Usage: post-results.sh {pr|baseline|comment-reply} [options]" >&2
        exit 1
        ;;
esac
